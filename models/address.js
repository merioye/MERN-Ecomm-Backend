const mongoose = require("mongoose");

const addressSchema = new mongoose.Schema({
    userId: {
        type: String,
        required: true,
    },
    country: {
        type: String,
        required: true,
    },
    state: {
        type: String,
        required: true,
    },
    city: {
        type: String,
        required: true,
    },
    zipCode: {
        type: String,
        required: true,
    },
    address1: {
        type: String,
        required: true,
    },
    address2: {
        type: String,
        default: "",
    },
});

module.exports = mongoose.model("Address", addressSchema);
